<script>
import classDetailService from "../services/ClassDetailService";

export default {
name: "class-table-list",
data() {
    return {
        dialog: false,
      dialogDelete: false,
      headers: [
        {
          text: 'Class Description',
          align: 'start',
          sortable: false,
          value: 'class_description',
        },
        { text: 'Teacher', value: 'teacher_name' },
        { text: 'Duration', value: 'class_duration' },
        { text: 'Class Time', value: 'class_datetime' },
      ],
      classes: [],
      editedIndex: -1,
      editedItem: {
        name: '',
        calories: 0,
        fat: 0,
        carbs: 0,
        protein: 0,
      },
      defaultItem: {
        name: '',
        calories: 0,
        fat: 0,
        carbs: 0,
        protein: 0,
      },
    }
},
created() {
    this.getClassTable()
},
methods: {
    getClassTable() {
        classDetailService.getAllClasses().then((response) => {
            if (response.status == 200) {
                this.$store.commit("SET_CLASS_LIST", response.data);
                this.classes = response.data
            } else {
                alert("Error retrieving class information")
            }   
        })
    },
    editItem (item) {
        this.editedIndex = this.desserts.indexOf(item)
        this.editedItem = Object.assign({}, item)
        this.dialog = true
      },

      deleteItem (item) {
        this.editedIndex = this.desserts.indexOf(item)
        this.editedItem = Object.assign({}, item)
        this.dialogDelete = true
      },

      deleteItemConfirm () {
        this.desserts.splice(this.editedIndex, 1)
        this.closeDelete()
      },

      close () {
        this.dialog = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.editedIndex = -1
        })
      },

      closeDelete () {
        this.dialogDelete = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.editedIndex = -1
        })
      },

      save () {
        if (this.editedIndex > -1) {
          Object.assign(this.desserts[this.editedIndex], this.editedItem)
        } else {
          this.desserts.push(this.editedItem)
        }
        this.close()
      },
},
computed: {
     formTitle () {
        return this.editedIndex === -1 ? 'Create Class' : 'Edit Class'
      },
},
watch: {
      dialog (val) {
        val || this.close()
      },
      dialogDelete (val) {
        val || this.closeDelete()
      },
    },

};
</script>

